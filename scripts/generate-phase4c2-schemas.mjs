function domainRow(name, title, description, moduleId, properties, required, extraAllOf = []) {
  const base = rowSchema({
    family: "domain",
    name,
    title,
    description,
    properties: {
      module_row_id: ref("ModuleRowID"),
      study_template_id: ref("StudyTemplateID"),
      module_id: { type: "string", const: moduleId },
      compatible_core_schema_version: ref("SemverRange"),
      module_schema_version: ref("SemverVersion"),
      visit_id: ref("VisitID"),
      procedure_id: ref("ProcedureID"),
      active_flag: ref("BooleanFlag"),
      ...properties,
    },
    required: [
      "module_row_id",
      "study_template_id",
      "module_id",
      "compatible_core_schema_version",
      "module_schema_version",
      "active_flag",
      ...required,
    ],
  });
  if (extraAllOf.length) base.allOf = extraAllOf;
  return base;
}

